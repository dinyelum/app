<h4>Both Teams to Score (Yes or No)</h4>
<p>This is often represented by BTTS or GG (Goal Goal). This is pretty much straight forward because obviously, when selecting this option, you already should know you should expect.</p>
<h5>BTTS-YES (GG)</h5> 
<p>Both Teams to score. Examples of successful outcomes include: 1-1, 1-2. 6-1, 4-2 etc. Both teams have to score for your bet to be successful.</p>
<h5>BTTS-NO (NGG)</h5>
<p>One or Both teams MUST NOT score. Examples of successful outcomes include: 0-0,0-1,4-0,0-2,3-0 etc. At least one of the teams must score NO GOAL in the match.</p>


<h3>Double Chance</h3>
<p>This betting option is quite easy to learn and master. The double chance is a great options because it provides extra security. The double chance option is of three major types: 1X, 12 and X2.</p>
<h5>1X</h5> 
<p>Selecting this option means you are betting on the Home Team to Win or at least DRAW the match. If somehow, the away team wins the match, the bet has been lost.</p>
<h5>12</h5> 
Betting on this option means there must be a win. Either the Home Team or the Away Team must win the match. The match must not end in a draw.
<h5>X2</h5>
<p>When you come across this otion in prediction sites on on betting sites, it means betting on the Away Team to Win or at least DRAW the match. If the Home team wins the match, the bet has been lost.</p>

