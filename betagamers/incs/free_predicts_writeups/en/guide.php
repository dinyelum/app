<h3>HANDICAP</h3>
<p>This is the type of bet market that is very common and is mostly used by the masters of Contrarian Betting. It involves adding a number of points / Goals to the Underdogs by way of giving them a Head Start before the game starts as well as subtracting the same number of points or goals from the favorites before the match starts. The Handicap Market is of two types: The European Handicap and the Asian Handicap.</p>

<h4>European Handicap</h4>
<p>The European Handicap is the most common in use and can be represented in various forms.</p>
<p>Examples: <br>
England -1 X -1 Spain 1 is the same thing as (0:1) 1H (0:1) XH (0:1) 2H<br><br>

AND <br>
England 1 X -1 Spain -1 is the same thing as (1:0) 1H (1:0) XH (1:0) 2H<br><br>

Likewise<br>
England -2 X -2 Spain 2 is the same thing as (0:2) 1H (0:2) XH (0:2) 2H<br><br>

AND <br>
England 2 X -2 Spain -2 is the same thing as (2:0) 1H (2:0) XH (2:0) 2H</p>

<p>Now, selecting England -1 from the first example is the same as selecting (0:1) 1H and it means England has to win the match by 2 goals or more for the bet to be won.</p>
<p><b>What do you mean?</b></p>
<p>In the above selection (England -1 or (0:1) 1H), we are giving Spain ONE GOAL ahead before the match is started, thus; we are assuming that England is loosing the match by 1 goal before the match is started and then we are betting on England to come from behind, from that one goal deficit to win the match.</p>
<p>Examples of the outcomes we expect are: 2:0, 3:1, 4:2 etc. England must win by 2 goals or more for the bet to be won. Anything less than 2 goals is a lost bet.</p>

<p>Selecting X-1 (or (0:1) XH) means England has to win the match by exactly 1 goal. If England wins by more than one goal, the bet is lost. Also, if the match ends in a draw, the bet is lost too.</p>

<p>Picking Spain 1 (or (0:1) 2H) requires Spain to Win or at least get a Draw from the match. If Spain looses, the bet is lost.</p>

<h4>Asian Handicap</h4>
<p>The Asian Handicap is very common in a lot of soccer picks websites. A typical example is:</p>
<p>Say Italy vs France and the Asian Handicap odds are listed as follows:
Italy (-1.5 at 2 odds) and France (+1.5 at 1.86 odds)</p>

<p>After the match, the 1.5 goals are added to France score to determine winning and losing bets. </p>
<p>For instance, a scoreline of Italy 3 : 1 France in this betting option actually means Italy 3 : 2.5 France and in this case, the bet is won.</p>
<p>1.5 Handicap bet requires Italy to win by 2 or more goals but if eventually, France wins the match or looses by less than two goals, the France (+1.5) wins.</p>

