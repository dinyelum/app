<p>A Ligue 1 é a liga profissional francesa de futebol masculino e a liga mais alta da França. Assim como muitas outras grandes ligas da Europa, é composta por 20 principais times do país que disputam o título da Ligue 1. Sua temporada padrão é composta por 38 partidas com cada time jogando contra o mesmo adversário dentro e fora de casa. No final de cada temporada/campanha: </p>
<ul>
<li>Os melhores times que terminarem em 1º e 2º lugares participam automaticamente da Competição da UCL na próxima temporada</li>
<li>O time que terminar em 3º joga nas eliminatórias da UEFA Champions League para garantir um lugar na fase de grupos da próxima temporada da Liga dos Campeões</li>
<li>O 4º time da tabela da Ligue 1 entra diretamente na disputa pelo título da Liga Europa para a próxima temporada</li>
<li>Os dois últimos times são rebaixados automaticamente para a Ligue 2, a segunda divisão do futebol francês.</li>
<li>O 17º lugar na tabela vai participar do play-off de rebaixamento para determinar se estará na Ligue 1 na próxima temporada ou se será rebaixado para a Ligue 2</li>
<li>Os times rebaixados são então substituídos po: 
<ul>
<li>os dois clubes promovidos da Ligue 2, se o 17º colocado se qualificar para jogar na Ligue 1 na próxima temporada</li>
<li>os três clubes promovidos da Ligue 2, se o 17º colocado não conseguir manter sua vaga na Ligue 1</li>
</ul></li>
</ul>
